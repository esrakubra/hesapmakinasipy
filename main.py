nub1= int(input('Birinci Sayıyı Giriniz:'))
nub2= int(input('İkinci Sayıyı Giriniz:'))

op = input('Bir İşlem Seçiniz(+, -, *, /):')

if op == '+':
    print(nub1, '+', nub2, '=', nub1 + nub2)
elif op == '-':
    print(nub1,'+', nub2, '=', nub1 + nub2)
elif op == '*':
    print(nub1,'+', nub2, '=', nub1 + nub2)
elif op == '/':
    print(nub1, '+', nub2, '=', nub1 + nub2)
else:
    print('Programımızda Böyle Bir İşlem Geçerli Değildi')



    
    
    
